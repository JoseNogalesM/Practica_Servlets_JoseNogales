
package com.emergentes;
public class Tareas {
    private int id;
    private String actividad;
public Tareas(){
this.id=0;
this.actividad="";
}

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getActividad() {
        return actividad;
    }

    public void setActividad(String actividad) {
        this.actividad = actividad;
    }

    
}